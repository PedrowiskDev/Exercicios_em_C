/* 6. Expressões e Explicações:

p++;: Isso aumenta o valor de p em uma unidade. 
Significa que o ponteiro p avança para o próximo elemento do tipo apontado por p. 

(*p)++;: Incremento do valor apontado por p. Se p apontar para uma variável essa expressão incrementará o valor da variável apontada por p.

*(p++);: Isso combina a operação de pós-incremento com a operação de desreferência. Primeiro, p é incrementado em uma unidade, e em seguida, o valor da variável apontada por p antes do incremento é acessado e usado na expressão.

A expressão *(p+10) se refere ao valor que está a 10 elementos de distância do elemento apontado por p. */